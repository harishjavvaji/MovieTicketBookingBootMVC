package com.movieticket.booking.models;


public class Demo {

    private String abc;
    private String def;

    public Demo(String abc, String def) {
        this.abc = abc;
        this.def = def;
    }

    public String getAbc() {
        return abc;
    }

    public void setAbc(String abc) {
        this.abc = abc;
    }

    public String getDef() {
        return def;
    }

    public void setDef(String def) {
        this.def = def;
    }
}
